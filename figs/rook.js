import Pair from '../aux/pair.js';

///////////////////////////////////////////////////////////////
// Model //
///////////

function* model(p) {
  for (var k=0; k<8; ++k) {
    if (k != p.b)
      yield new Pair(p.a, k)
    if (k != p.a)
      yield new Pair(k, p.b)
  }
}

//////////////////////////////////////////////////////////////
// View //
//////////


//////////////////////////////////////////////////////////////
// Controller //
////////////////

export function getFields(i, j) {
  var p = new Pair(i, j)
  if (p.valid()) return model(p)
  return false
}
