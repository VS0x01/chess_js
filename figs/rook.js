import Pair from '../aux/pair.js';
import {getController} from './_common_controller.js';

///////////////////////////////////////////////////////////////
// Model //
///////////

function* model(p) {
  for (var k=0; k<8; ++k) {
    if (k != p.b)
      yield new Pair(p.a, k)
    if (k != p.a)
      yield new Pair(k, p.b)
  }
}

//////////////////////////////////////////////////////////////
// View //
//////////


//////////////////////////////////////////////////////////////
// Controller //
////////////////

export var getFields = getController(model)

